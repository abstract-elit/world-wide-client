export const convertCountryCode = code => {
  const newCode = code.toUpperCase();
  return isoCountriesCodes[newCode].name;
};

export const convertCountryName = name => {
  name = name
    .toLowerCase()
    .split(' ')
    .map(s => s.charAt(0).toUpperCase() + s.substring(1))
    .join(' ');
  return isoCountriesNames[name];
};

